    public void clearTable(JTable tbl) {
        int rows = tbl.getRowCount();
        int column = tbl.getColumnCount();
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < column; j++) {
                tbl.setValueAt(null, i, j);
            }
        }
    }

    public void viewEntryTable() {
        String MouzaName = combo_mouzaEntry.getSelectedItem().toString();
        String ZoneName = combo_zoneEntry.getSelectedItem().toString();
        clearTable(tbl_viewTable);
        String query = "select tbl_daagNo.DaagNo from tbl_mouza inner join tbl_daagNo on tbl_mouza.MouzaId = tbl_daagNo.MouzaID inner join tbl_zone on tbl_zone.ZoneId = tbl_daagNo.ZoneID where tbl_mouza.MouzaName = '" + MouzaName + "' and   tbl_zone.ZoneName = '" + ZoneName + "' Order by tbl_DaagNo.daagId";
        try {
            pst = conn.prepareStatement(query);
            rs = pst.executeQuery();
            int i = 0, j = 0;

            while (rs.next()) {

                String daagNo = rs.getString("DaagNo");

                tbl_entryTable.setValueAt(daagNo, i, j++);

                if (i == tbl_entryTable.getRowCount()) {
                    break;
                }

                if (j == tbl_entryTable.getColumnCount()) {
                    j = 0;
                    i++;
                }

            }
            label_mouzaZoneEntry.setText("MOUZA: " + MouzaName + " ||  ZONE: " + ZoneName);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private int getCurrentEntryStatus() {
        int status = 0;
        try {

            String query = "select status from tbl_entryStatus where id = 1 ";
            pst = conn.prepareStatement(query);
            rs = pst.executeQuery();

            while (rs.next()) {
                status = Integer.parseInt(rs.getString("status"));

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        if (status == 1) {
            lable_currentStatus.setText("Currently Data Entry is ON ");
            lable_currentStatus.setForeground(new Color(0, 102, 0));
            btn_toggleEntry.setText("Turn off");

        } else {
            lable_currentStatus.setText("Currently Data Entry is OFF ");
            lable_currentStatus.setForeground(Color.red);
            btn_toggleEntry.setText("Turn On");
        }

        return status;
    }

    public void dataEntryenableDisable() {
        int status = getCurrentEntryStatus();
        if (status == 0) {
            btn_insert.setVisible(false);
            btn_delete.setVisible(false);
            pnl_update.setVisible(false);
            label_entryStatus.setVisible(true);
            label_entryStatus.setText("Currently Data Entry is disabled. Contact Administrator!!");
        } else {
            btn_insert.setVisible(true);
            btn_delete.setVisible(true);
            pnl_update.setVisible(true);
            label_entryStatus.setVisible(false);
        }

    }

