    private void bnt_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bnt_printActionPerformed
        loadViewTable();
        JFileChooser chooser = new JFileChooser();

        chooser.setCurrentDirectory(new java.io.File("."));
        chooser.setDialogTitle("Select Destination folder");
        chooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        chooser.setAcceptAllFileFilterUsed(false);
        String selectedDirectory = "";
        if (chooser.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            //currentDirectory = chooser.getCurrentDirectory().toString();
            selectedDirectory = chooser.getSelectedFile().toString();

        } else {
            JOptionPane.showMessageDialog(null, "No selection");
        }

        Font normalFont = new Font(Font.FontFamily.TIMES_ROMAN, 12, Font.NORMAL);
        Font boldFont = new Font(Font.FontFamily.TIMES_ROMAN, 15, Font.BOLD);
        try {

            Document doc = new Document(PageSize.A4);
            PdfWriter.getInstance(doc, new FileOutputStream(selectedDirectory + "\\" + combo_mouzaView.getSelectedItem() + "__" + combo_zoneView.getSelectedItem() + ".pdf"));
            doc.open();
            PdfPTable pdfTable = new PdfPTable(10);
            pdfTable.setWidthPercentage(100);
            doc.add(new Paragraph(" " + label_mouzaZoneview.getText(), boldFont));
            doc.add(new Paragraph(" "));
            for (String daagAra1 : daagAra) {
                Phrase data = new Phrase(daagAra1, normalFont);
                pdfTable.addCell(data);
            }

            doc.add(pdfTable);
            doc.close();
            JOptionPane.showMessageDialog(null, label_mouzaZoneview.getText() + "  \n Successfully saved as PDF");

        } catch (DocumentException ex) {

        } catch (FileNotFoundException ex) {

        }


    }//GEN-LAST:event_bnt_printActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed

        int mouzaId = combo_mouzaEntry.getSelectedIndex() + 1;
        int zoneId = combo_zoneEntry.getSelectedIndex() + 1;
        String old = txt_oldValueEntry.getText();
        int newValue = Integer.parseInt(txt_newValueEntry.getText());
        if (!"".equals(old) || !"".equals(newValue)) {
            try {
                String query = "select * from tbl_daagNo where tbl_daagNo.MouzaId = '" + mouzaId + "' and   tbl_daagNo.ZoneId = '" + zoneId + "' and daagNo = '" + newValue + "' ";
                pst = conn.prepareStatement(query);
                rs = pst.executeQuery();

                //This logic is validating id new daag is already  included  under a zone under a mouza
                int count = 0;
                while (rs.next()) {
                    ++count;

                }
                if (count == 0) {
                    query = "update tbl_daagNo set daagNo = '" + newValue + "' where daagNo = '" + old + "' and mouzaId = '" + mouzaId + "' and zoneId = '" + zoneId + "' ";
                    pst = conn.prepareStatement(query);
                    if (pst.executeUpdate() > 0) {
                        JOptionPane.showMessageDialog(null, "Daag No : " + old + " has been Updated to " + newValue);
                    } else {
                        JOptionPane.showMessageDialog(null, "No daag found for : " + old + "\n Update unsuccessful");
                    }
                    //clearTable();
                    viewEntryTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Daag :" + newValue + " is already present in \n MOUZA: " + combo_mouzaEntry.getSelectedItem().toString() + " and ZONE: " + combo_zoneEntry.getSelectedItem().toString());
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else
        {
             JOptionPane.showMessageDialog(null, "Fill the fields properly!!! Update unsuccessful");
        }


    }//GEN-LAST:event_btn_updateActionPerformed

