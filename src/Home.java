    private void loadViewTable() {

        String MouzaName = combo_mouzaView.getSelectedItem().toString();
        String ZoneName = combo_zoneView.getSelectedItem().toString();
        String query = "select tbl_daagNo.DaagNo from tbl_mouza inner join tbl_daagNo on tbl_mouza.MouzaId = tbl_daagNo.MouzaID inner join tbl_zone on tbl_zone.ZoneId = tbl_daagNo.ZoneID where tbl_mouza.MouzaName = '" + MouzaName + "' and   tbl_zone.ZoneName = '" + ZoneName + "' Order by tbl_DaagNo.DaagId";
        try {
            pst = conn.prepareStatement(query);
            rs = pst.executeQuery();
            int i = 0, j = 0, k = 0;
            while (rs.next()) {

                String daagNo = rs.getString("DaagNo");
                tbl_viewTable.setValueAt(daagNo, i, j++);
                daagAra[k++] = daagNo;
                if (i == tbl_viewTable.getRowCount()) {
                    break;
                }

                if (j == tbl_viewTable.getColumnCount()) {
                    j = 0;
                    i++;
                }

            }
            label_mouzaZoneview.setText("MOUZA: " + MouzaName + "  ||  ZONE: " + ZoneName);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

