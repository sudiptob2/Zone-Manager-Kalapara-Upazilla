    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed

        try {

            int rows = tbl_entryTable.getRowCount();
            int column = tbl_entryTable.getColumnCount();
            String daagNo = "";
            int insertCount = 0;
            int total = 0;
            int duplicate = 0;
            for (int i = 0; i < rows; i++) {
                for (int j = 0; j < column; j++) {
                    int mouzaId = combo_mouzaEntry.getSelectedIndex() + 1;
                    int zoneId = combo_zoneEntry.getSelectedIndex() + 1;

                    //JOptionPane.showMessageDialog(null, mouzaId);
                    daagNo = (String) tbl_entryTable.getValueAt(i, j);
                    if (daagNo == null) {
                        continue;
                    }
                    total++;
                    String query = "select * from tbl_daagNo where tbl_daagNo.MouzaId = '" + mouzaId + "' and   tbl_daagNo.ZoneId = '" + zoneId + "' and daagNo = '" + daagNo + "' ";
                    pst = conn.prepareStatement(query);
                    rs = pst.executeQuery();

                    //This logic is skipping once included daag under a zone under a mouza
                    int count = 0;
                    while (rs.next()) {
                        ++count;

                    }

                    //JOptionPane.showMessageDialog(null, "Coutt: " + count);
                    if (count == 0) {
                        insertCount++;
                        //JOptionPane.showMessageDialog(this, "Execute update: ");
                        //The daag no is not inserted before so we insert it
                        query = "INSERT INTO tbl_daagNo(DaagNo,MouzaId,ZoneId) VALUES(?,?,?)";
                        pst = conn.prepareStatement(query);
                        pst.setInt(1, Integer.parseInt(daagNo));
                        pst.setInt(2, mouzaId);
                        pst.setInt(3, zoneId);
                        pst.executeUpdate();

                    }
                    duplicate++;
                }

            }
            JOptionPane.showMessageDialog(null, "Total Daag Entered : " + total + "\nTotal Daag Inserted: " + insertCount + "\n " + (total - insertCount) + " Duplicate entry found and skipped");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid Daag No :" + e.getMessage());
        }
    }//GEN-LAST:event_btn_insertActionPerformed

